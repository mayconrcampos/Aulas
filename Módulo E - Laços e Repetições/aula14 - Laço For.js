/**
 * Laço For - Laços com repetições pré programadas.
 */

 // Vamos fazer a tabuada de 1 até 10 usando um laço For dentro de outro laço For.

 for(i = 1; i <= 10; i++){
     console.log(`Tabuada de ${i}`)
     for(j = 1; j <= 10; j++){
         console.log(`${i} x ${j} = ${i * j}`)
     }
 }

